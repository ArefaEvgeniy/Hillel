def factorial(n):
    """
    Обчислює факторіал числа n.

    :param n: Ціле число.
    :return: Факторіал числа n.
    """
    # Базовий випадок: 0! та 1! рівні 1
    if n == 0 or n == 1:
        return 1
    else:
        # Рекурсивний виклик: n! = n * (n-1)!
        return n * factorial(n - 1)


# 5! = 5 * 4 * 3 * 2 * 1 = 120
# 10! = 10 * 9 * 8 * ... * 2 * 1 = 3628800
