def my_func(a, b):
    """
    Ця функція приймає два аргументи і повертає їх суму.
    Приклад використання:
    result = my_func(3, 5)
    print(result)  # Виведе: 8
    :param a: int - перший аргумент, який буде додано до другого.
    :param b: int - другий аргумент, який буде додано до першого.
    :return: int - сума двох аргументів.
    """
    return a + b


